#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		j;
	int		n;

	i = 0;
	n = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		i++;
	if (str[i] == '-')
		j = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (*str >= '0' && *str <= '9')
	{
		n = 10 * n + j * (str[i] - '0');
		i++;
	}
	return (n);
}
